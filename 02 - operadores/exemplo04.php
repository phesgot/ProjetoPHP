<?php
// Operadores de comparação:

$a = 55.0;
$b = 55;

var_dump($a < $b); // Menor.
echo "<br/>";
var_dump($a > $b); // Maior.
echo "<br/>";
var_dump($a == $b); // Igual para valores.
echo "<br/>";
var_dump($a === $b); // Igual compara tipo e valor.
echo "<br/>";
var_dump($a != $b); // diferente para valores.
echo "<br/>";
var_dump($a !== $b); // diferente compara tipo e valor.