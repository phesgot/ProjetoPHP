<?php
//Operadores incrementais e decrementais:

$a = 10;

echo $a++; // Primeiro exibe e depois incrementa

echo "<br/>";

echo $a; // Resultado = 11

echo "<br/>";

echo ++$a; // Primeiro incrementa depois exibe = 12;

echo "<br/>";
echo "<br/>";

echo $a--;

echo "<br/>";

echo $a;

echo "<br/>";

echo --$a;
?>